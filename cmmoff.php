                    <h2>History</h2>  
                      
<p class="p" align="justify">Mandi Division comprising of five districts is headed by the Commissioner, Mandi. Each district is headed by a Deputy Commissioner who has under him an Additional Deputy Commissioner, SDM, City Magistrate, District Revenue Officer, and Tehsildar/Sub Registrars. The Deputy Commissioner of five districts of Mandi Division report to the Divisional Commissioner. He is the controlling and supervisory officer of their work. Divisional Commissioner also coordinates working of all the department at the divisional level to effect synergy and sort out inter department problems. Divisional commissioner keeps state and government authorities informed of all developments and problems and to seek assistance and guidance from those quarters. Divisional Commissioner is also the Head of the Revenue Department at the Division level.. Divisional Commissioner also exercise quasi judicial powers under Punjab land revenue Act 1887, Punjab Village Common Lands (Regulations), Punjab tenancy Act 1887 & Punjab Security of Land Tenures Act H.P Public Premises Act, Indian Stamp Act, Indian Arms Act, H.P Municipal corporation Act Punjab village occupancy Rights Act.
