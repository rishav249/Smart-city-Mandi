<h2>History</h2>

<hr>
<p>
The present District Of Mandi was formed with the merger of two princely states Mandi and Suket on 15th April 1948, when the State of Himachal Pradesh came into existence. Ever since the formation of the district, it has not witnessd any changes in it’s jurisdiction. The chiefs of Mandi and Suket are said to be from a common ancestor of the Chandravanshi line of Rajputs of Sena dynasty of Bengal and they claim their descent from the Pandavas of the Mahabharata. The ancestors of the line believed to have ruled for 1,700 years in Indarprastha(Delhi), until one Khemraj was driven out by his Wazir, Bisarp, who then took over the throne. Khemraj, having lost his knogdom, fled eastward and settled in Bengal, where 13 of his successors are said to have ruled for 350 years. From there they had to flee to Ropar in Punjab, but here also the king, Rup Sen, was killed and one of his sons, Bir Sen, fled to the hills and reached Suket. The State of Suket is said to have been founded by Bir Sen, an ancestor of the Sena Dynasty of Bengal.<br>
The seperation of Mandi from suket took place about the year 1200 AD. Upto that time, it was the single state of Suket. The then reigning chief Sahu Sen had a quarrel with his younger brother Bahu Sen, who left Suket to seek his fortunes elsewhere. Bahu Sen after leaving Suket settled at Manglan in Kullu, where his descendents lived for 11 generations. Then then chief, Kranchan Sen was killed fighting against the Kullu Raja and his Rani, who was pregnant at that time. fled alone to her father who was the chief of Seokot, who had no son. Here she gave birth to a baby boy who was named Ban, the tree under which he was born. Ban had hardly crossed the age of 15 when he defeated the chief of Kilti who used to plunder travellers.On the death of the chief of Seokot, Ban succeeded to the chiefship of Seokot. He, after some time killed the Rana of Sakor and took posesssion of his lands. he then changed his residence to Bhiu, on the banks of Beas and a few miles from the present Mandi town. Mandi emerged as a seperate state in the begining of the sixteenth century. Down the line of descendants of Ban came Ajbar Sen, nineteenth in descent from Bahu Sen, who founded Mandi Town in 1527 AD, the capital of the erstwhile state of Mandi and the headquarters of the now Mandi District.<br>
Ajbar Sen was the first great ruler of Mandi. He was probably the first to assume the designation of Raja. He consolidated the territories that he had inherited and added to them new ones that he wrested from the hands of his neighbours. He built a palace here and adorned it with four towers. He also built the temple of Bhut Nath and his Rani constructed that of Trilok Nath. Down the line of descendants was Raja Sidh Sen, who succeeded Raja Gur Sen in 1978 AD. Mandi had never been so powerful before his reign and after that never was. He captured great areas from the adjoining areas. it was during his reign that Guru Govind Singh, the tenth guru of the sikhs visited Mandi in the close of the 17th century. He had been imprisoned by Raja singh, the chief of Kullu, from whom he had sought assistance against Mughal troops and his followers believe that Guruji escaped by using miraculous powers. Raja Sidh Sen, who is also considered to be a posessor of great miraculous powers, entertained his with great hospitality. He built the great tank before the palace.
<br>
He also built the temples of "Sidh Ganesha" and "Trilokinath". The entire history of both the states of Mandi and Suket is littered with wars with among themselves and other adjoining states. These two states had always been rivals and generally enemies, but there was no great result of their warfare. The fertile valley of Balh was the common ground of desire and dispute. On 21st February 1846 the chiefs of Mandi and Suket visited Mr. Erskine, Suprintendent of the Hill States for the British Government, owing their allegiance to the Britishers and securing their protection. on 9th march, 1846 a treaty was concluded between the British Government and the Sikh Durbar whereby the whole of the Doab area between the Beas and the Sutlej was ceeded to the British Government, and this included the ststes of Mandi and Suket also. On 1st November 1921, both the state of Mandi and Suket were transferred from the political control of Punjab Government to that of Government of India till 15th August 1947, India’s Independence Day.



